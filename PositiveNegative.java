package com.demo;

/*Given number: num = 912

1. Check the condition: (num > 0)
   912 is greater than 0, so print: "The number is positive."

Final output: The number is positive.
*/

public class PositiveNegative {
	public static void main(String[] args)   
	{  
	int num=912;  
	if(num>0)  
	{  
	System.out.println("The number is positive.");  
	}  
	else if(num<0)  
	{  
	System.out.println("The number is negative.");  
	}  
	else  
	{  
	System.out.println("The number is zero.");  
	}  
	}  
	}  

